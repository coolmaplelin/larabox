<?php

namespace App\Http\Controllers\Auth;

use App\Models\User;
use App\Models\Newsletter;
use App\Models\UserNewsletters;
use App\Http\Controllers\Controller;
use Illuminate\Support\Facades\Validator;
use Illuminate\Foundation\Auth\RegistersUsers;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;

class RegisterController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | Register Controller
    |--------------------------------------------------------------------------
    |
    | This controller handles the registration of new users as well as their
    | validation and creation. By default this controller uses a trait to
    | provide this functionality without requiring any additional code.
    |
    */

    use RegistersUsers;

    /**
     * Where to redirect users after registration.
     *
     * @var string
     */
    protected $redirectTo = '/home';

    /**
     * Create a new controller instance.
     *
     * @return void
     */
    public function __construct()
    {
        $this->middleware('guest');
    }

    /**
     * Show the application registration form.
     *
     * @return \Illuminate\Http\Response
     */
    public function showRegistrationForm()
    {
        $newsletters = Newsletter::all();

        return view('auth.register', ['newsletters' => $newsletters]);
    }

    /**
     * Get a validator for an incoming registration request.
     *
     * @param  array  $data
     * @return \Illuminate\Contracts\Validation\Validator
     */
    protected function validator(array $data)
    {
        $messages = [
            'newsletter.user_newsletter' => 'You can only choose two newsletters',
        ];

        Validator::extendImplicit('user_newsletter', function ($attribute, $value, $parameters, $validator) {

            if (is_array($value) && count($value) > 2) {
                return false;
            }else{
                return true;
            }
            
        });

        return Validator::make($data, [
            'first_name' => 'required|max:255',
            'last_name' => 'required|max:255',
            'email' => 'required|email|max:255|unique:users',
            'password' => 'required|min:6|confirmed',
            'newsletter' => 'user_newsletter',  //This validator rule is defined in App\Providers\AppServiceProvider
        ], $messages);
    }

    /**
     * Create a new user instance after a valid registration.
     *
     * @param  array  $data
     * @return User
     */
    protected function create(array $data)
    {
        return User::create([
            'first_name' => $data['first_name'],
            'last_name' => $data['last_name'],
            'email' => $data['email'],
            'password' => bcrypt($data['password']),
            'account_type' => 'USER'
        ]);
    }

    /**
     * The user has been registered.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  mixed  $user
     * @return mixed
     */
    protected function registered(Request $request, $user)
    {
        //Insert newsletter records
        foreach($request->get('newsletter') as $newsletter_id) {

            // DB::table('user_newsletters')->insert(
            //     ['user_id' => $user->id, 'newsletter_id' => $newsletter_id]
            // );

            UserNewsletters::create([
                'user_id' => $user->id,
                'newsletter_id' => $newsletter_id,
            ]);
        }
    }
}
